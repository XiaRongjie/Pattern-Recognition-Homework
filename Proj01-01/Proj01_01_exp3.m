clc; clear;
%%确定一个二维的均值矢量和协方差矩阵，然后利用matlab中的meshgrid函数生成一个二维网格;
u = [0, 0];%均值
sigma = [5 0; 0 5];%协方差矩阵
[X, Y]  = meshgrid(-10: 0.1: 10, -10: 0.1: 10);

%%利用mvnpdf函数计算在每个网格点上的概率密度函数值，并绘制出这些函数值的三维曲面图。
p = mvnpdf([X(:) Y(:)], u, sigma);
p = reshape(p, size(X));%将行向量转换为矩阵向量
mesh(X, Y, p);
title('二元高斯分布概率密度函数值的三维曲面图');